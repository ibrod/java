public class Goods {
    int id;
    String name;
    String brand;
    float price;
    int inventory;
    public Goods(int id, String name, String brand, float price, int inventory) {
        this.id = id;
        this.name = name;
        this.brand = brand;
        this.price = price;
        this.inventory = inventory;
    }
    public Goods(){};
}
